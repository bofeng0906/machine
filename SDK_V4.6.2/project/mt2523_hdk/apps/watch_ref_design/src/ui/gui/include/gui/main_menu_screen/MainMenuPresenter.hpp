    virtual void timeUpdated(Time time);
    virtual void heartRateDataUpdated(int data);

    virtual ~MainMenuPresenter() {};

    void demoSelected(DemoID id, uint8_t menuIndex);

    void setSelectedMenuIndex(uint8_t menuIndex);

